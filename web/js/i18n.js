// 全局 i18n 工具
let currentLang = localStorage.getItem('survey_lang') || 'zh';

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('survey_lang', lang);
  // 需要 Vue app 重新渲染 —— app 会 watch lang 变化
  if (window.__app) {
    window.__app.lang = lang;
  }
}
